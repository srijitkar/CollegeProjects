            <h1>A young musician is reviving the glory of poetry through contemporary music</h1>
            <div class="post_commentbox"> <a href="#"><i class="fa fa-user"></i>Souvik Banerjee</a> <span><i class="fa fa-calendar"></i>6:49 AM</span> <a href="#"><i class="fa fa-tags"></i>Music</a> </div>

               <div class="single_page_content"> <img class="img-center" src="../images/4.gif" alt="">
        
                

              <p>Chinmayi Tripathi, a Delhi based young singer-songwriter has taken up a unique initiative to bring back the most precious poems of Hindi Literature and turn them into soulful songs.Poetry has been an intrinsic part of music and there is no denying that the words of poet-cum-musicians Amir Khusrow, Kabir, Rabindranath Tagore and Mirza Galib have inspired many generations of musicians in India. Though modern age trend has changed, there are still a few musicians who are trying to bring back the lost era of meaningful and heart touching lyrics.Chinmayi Tripathi, a Delhi based young singer-songwriter has taken up a unique initiative to bring back the most precious poems of Hindi Literature and turn them into soulful songs.The poems ranging from the inspirational Jaag Tujhko Dur Jana by Mahadevi Verma to Pagal Nalla, based on a folk tale by Harivansh Rai Bachchan. "The criteria for choosing the songs were mainly the lyrics. For instance, Pattwar by Shiv Mangal Suman is so passionate and full of vigor that if you recite it to a corpse, he will come alive. It's meant for someone who is feeling defeated. </p>
              <blockquote>The words spur you to do something with your life." she says.The album has been officially launched at the Music & Poetry Festival, CP which was presented by NDMC and supported by Goodyear and Songdew.com.The first song has hit the digital space and is currently the talk of the town. Listen to the first song – ‘Khushgappiyan’. </blockquote>
